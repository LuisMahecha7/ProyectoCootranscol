    <h2>
La Cooperativa Integral de Transportadores Colonial, “Cootranscol”, es una empresa dinámica, generadora de valor social y económico para lograr el desarrollo Integral de sus asociados, empleados y directivos, en torno a la aplicación y promulgación de los valores y principios de la economía solidaria, para lo cual debe prestar un servicio de transporte multimodal, en la ciudad de Tunja, el departamento de Boyacá y la república de Colombia, que sea eficiente y eficaz y con estándares de calidad que permitan proyectar a “Cootranscol”, como la mejor empresa en la prestación del servicio de transporte urbano al menos en la ciudad de Tunja.

En Cinco años, la Cooperativa Integral de Transportadores Colonial, “Cootranscol”, tendrá los recursos y el liderazgo suficiente, para constituirse en una entidad sólida financiera y socialmente, llegando a niveles de competitividad que le permita liderar la aplicación de los principios de la solidaridad, a nivel de la región, el departamento y de la nación, constituyéndose en la mejor propuesta para prestación de transporte multimodal, a nivel regional.
